#include <stdio.h>
#include <stdlib.h>

void main(){
    // para imprimir um número inteiro usa-se %d ou %i
    printf("%d",400);
    printf("\n");
    printf("%i",400);
    printf("\n");

    // para imprimir uma string usa-se %s
    printf("%s", "Jeiverson");
    printf("\n");

    system("pause");
}
